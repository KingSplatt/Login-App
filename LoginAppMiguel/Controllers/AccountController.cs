using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Identity;
using LoginAppMiguel.Models;
using LoginAppMiguel.Models.ViewModels;
using Microsoft.EntityFrameworkCore;

namespace LoginAppMiguel.Controllers
{
    public class AccountController : Controller
    {
        private readonly UserManager<User> _userManager;
        private readonly SignInManager<User> _signInManager;

        public AccountController(UserManager<User> userManager, SignInManager<User> signInManager)
        {
            _userManager = userManager;
            _signInManager = signInManager;
        }

        [HttpGet]
        public IActionResult Register()
        {
            if (User.Identity.IsAuthenticated)
            {
                return RedirectToAction("Index", "Users");
            }
            return View();
        }

        [HttpPost]
        public async Task<IActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid){
                var user = new User{
                    UserName = model.Email,
                    Email = model.Email,
                    FullName = model.FullName,
                    RegistrationTime = DateTime.UtcNow
                };
                try{
                    var result = await _userManager.CreateAsync(user, model.Password);
                    if (result.Succeeded){
                        return RedirectToAction("Login", "Account");
                    }
                    foreach (var error in result.Errors){
                        if (error.Code == "DuplicateUserName" || error.Code == "DuplicateEmail"){
                            ModelState.AddModelError("Email", "This email is already registeredee.");
                            break;
                        }
                    }
                }
                catch (DbUpdateException ex){
                    if (ex.InnerException?.Message.Contains("Duplicate entry") == true ||
                        ex.InnerException?.Message.Contains("UNIQUE constraint") == true ||
                        ex.InnerException?.Message.Contains("duplicate key") == true){
                        ModelState.AddModelError("Email", "This email is already registered.");
                    }else{
                        ModelState.AddModelError(string.Empty, "An error occurred while creating the account. Please try again.");
                    }
                }
            }
            return View(model);
        }

        [HttpGet]
        public IActionResult Login()
        {
            if (User.Identity.IsAuthenticated)
            {
                return RedirectToAction("Index", "Users");
            }
            return View();
        }

        [HttpPost]
        public async Task<IActionResult> Login(LoginViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = await _userManager.FindByEmailAsync(model.Email);
                if (user != null)
                {
                    if (user.IsBlocked)
                    {
                        ModelState.AddModelError(string.Empty, "Your account has been blocked. Contact the admin.");
                        return View(model);
                    }
                    var result = await _signInManager.PasswordSignInAsync(user.UserName, model.Password, model.RememberMe, lockoutOnFailure: false);
                    if (result.Succeeded)
                    {
                        user.LastLoginTime = DateTime.UtcNow;
                        await _userManager.UpdateAsync(user);
                        return RedirectToAction("Index", "Users");
                    }
                }
                ModelState.AddModelError(string.Empty, "Email or password is incorrect");
            }

            return View(model);
        }

        [HttpPost]
        public async Task<IActionResult> Logout()
        {
            await _signInManager.SignOutAsync();
            return RedirectToAction("Login");
        }

        public IActionResult AccessDenied()
        {
            return View();
        }
    }
}
